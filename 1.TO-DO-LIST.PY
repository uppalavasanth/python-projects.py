tasks = []

while True:
    print("\n1. Add Task")
    print("2. View Tasks")
    print("3. Remove Task")
    print("4. Exit")

    choice = input("Enter choice: ")

    if choice == "1":
        task = input("Enter task: ")
        tasks.append(task)

    elif choice == "2":
        print("\nYour Tasks:")
        for t in tasks:
            print("-", t)

    elif choice == "3":
        task = input("Enter task to remove: ")
        tasks.remove(task)

    elif choice == "4":
        break